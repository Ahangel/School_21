int		ft_sqrt(int nb)
{
	long int sqr;

	sqr = 0;
	while (1)
	{
		if (sqr * sqr > nb)
			return (0);
		if (sqr * sqr == nb)
			return ((int)sqr);
		sqr++;
	}
}
