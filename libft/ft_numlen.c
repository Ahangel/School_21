#include "libft.h"

int		ft_numlen(const char *s)
{
	int size;

	size = 0;
	if (*s == '-')
	{
		size++;
		s++;
	}
	while (*s && ft_isdigit(*s))
	{
		size++;
		s++;
	}
	return (size);
}
